        <h5 class="h5">
            Die Wilgers is one of the eastern suburb of Pretoria, Gauteng.
            It can be described as a safe residential area, rich in trees and with beautiful gardens and sidewalks.
            The presence of an Afrikaans and English high school in the area, and two primary schools nearby, as well as the German school in close proximity, makes Die Wilgers a sought after suburb.
            The residents of Die Wilgers strive to maintain the special character of the area through the actions of a Residents Association represented by an executive committee which is democratically elected at its public annual general meeting.
            The association represents residents in the area enclosed by Rubida Street to the West, Simon Vermooten Road to the East, the N4 to the North and Bronberg as the Southern border.

        </h5>
    </div>
    <div class="col-md-6">
        <h5 class="h5">With the help of seconded members, the committee aims to:</h5>
        <div class="list-group">
            <li class="list-group-item">
                Set up formal channels of communication between the community and official structures such as the City Council, South African Police Service and the Metro Police.
            </li>
            <li class="list-group-item">
                Address safety issues and maintain a neighbourhood network.
            </li>
            <li class="list-group-item">
                Address such as commercial developments, littering, vagrants and traffic.
            </li>
            <li class="list-group-item">
                Take up domestic issues such as road maintenance, street lighting, structures and sewerage.
            </li>
            <li class="list-group-item">
                Look after the declared open spaces in the area such as Oulandsloop and the tennis courts.
            </li>
        </div>
